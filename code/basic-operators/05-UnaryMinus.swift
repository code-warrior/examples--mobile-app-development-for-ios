let one = 1
let minusOne = -one
let positiveOne = -minusOne

print("Constant “one” is \(one)")
print("Constant “minusOne” is \(minusOne)")
print("And, constant “positiveOne” is \(positiveOne)")
print(minusOne * minusOne)
print("Recall that multiplying two negatives yields a positive:",
  "\(positiveOne)")
print("And multiplying a positive and a negative yields a negative:",
  "\(positiveOne * minusOne)")
